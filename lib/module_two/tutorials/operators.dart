void main() {
  
  // Arithmetic Operator
  print(44 + 3);
  print(55 - 5);
  print(45 * 5);
  print(66 / 2);
  
  // Equality Operator
  print(44 == 45);
  print(44 != 55);
  
  // Increment and Decrement Operator
  var number = 4;
  print(++number);
  print(number++);
  
  var anotherNumber = 4;
  print(--anotherNumber);
  print(anotherNumber--);
  
  // Compound Assignment Operator
  var  firstNumber = 5;
  firstNumber += 2;
  print(firstNumber);
  
  var secondNumber = 6;
  secondNumber -= 5;
  print(secondNumber);
  
  // Comparison Operator
  print(43 < 50);
  print(43 <= 43);
  print(45 > 30);
  print(45 >= 45);
  
  // Modulo Operator
  print(45 % 2);
  
  // Logical Operator
  print(34 < 45 && 34 > 45);
  print(34 < 45 || 34 > 45);
  
  // Negation Operator
  print(!(34 < 45));
}
